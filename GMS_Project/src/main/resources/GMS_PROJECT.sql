CREATE DATABASE GMS_PROJECT;
USE GMS_PROJECT;

CREATE TABLE GALLERY_MEMBER(
	GALLERY_ID 		VARCHAR(50) PRIMARY KEY,
    GALLERY_NAME 		VARCHAR(50),
    GALLERY_PASSWORD 		VARCHAR(500),
    GALLERY_NICKNAME 	VARCHAR(50),
    GALLERY_EMAIL 	VARCHAR(50),
	ROADADDRESS 		VARCHAR(500),
	JIBUNADDRESS 		VARCHAR(500),
	NAMUJIADDRESS 		VARCHAR(500),
    ZIPCODE 			VARCHAR(20),
	JOIN_DATE 			DATE
);

CREATE TABLE GALLERY_ADMIN(
	ART_TITLE 	VARCHAR(50),
    ART_CONTENT  VARCHAR(50),
   	ART_SELL 	INT,
    ART_STATUS  VARCHAR(50),
    ART_ARTNUM  INT PRIMARY KEY AUTO_INCREMENT,
    ARTIST VARCHAR(50),
    ARTIST_ART VARCHAR(50),
    ARTIST_COMENT VARCHAR(500),
    MAIN_ARTIST   VARCHAR(100),
    SHOW_NAME			VARCHAR(100),
    START_DATE			DATE,
    END_DATE			DATE,
    MAIN_ART      VARCHAR(500),
    ART_IMAGE     VARCHAR(500),
    ART_PRICE         INT,
    DELIVERY_PRICE    INT
);
CREATE TABLE GALLERY_ARTIST(
	ARTIST_ID 	INT PRIMARY KEY AUTO_INCREMENT,
    ARTIST VARCHAR(50),
    ARTIST_IMAGE VARCHAR(500),
    ARTIST_COMENT VARCHAR(500),
    MAIN_ARTIST   VARCHAR(100)
);

CREATE TABLE GALLERY_SHOP(
	ORDER_ID 			INT PRIMARY KEY AUTO_INCREMENT,
    GALLERY_ID 			VARCHAR(50),
	ART_ARTNUM			INT,
	ART_TITLE 		VARCHAR(100),
    ART_PRICE 	INT,
    GALLERY_EMAIL 	VARCHAR(50),
    GALLERY_NAME 		VARCHAR(50),
	RECEIVER_NAME 		VARCHAR(50),
	DELIVERY_ADDRESS 	VARCHAR(500),
    DELIVERY_METHOD 	VARCHAR(40),
    DELIVERY_MESSAGE 	VARCHAR(300),
	PAY_METHOD 			VARCHAR(200),
    PAY_ORDER_TIME 		DATE,
	CARD_COM_NAME 		VARCHAR(50),
	CARD_PAY_MONTH 		VARCHAR(20),
    DELIVERY_STATE 		VARCHAR(100)
);

CREATE TABLE GALLERY_REVIEW(
	ART_TITLE 		VARCHAR(100),
    GALLERY_ID    VARCHAR(100),
    GALLERY_NICKNAME 	VARCHAR(100),
    REVIEW_DATE DATE,
    REVIEW_TITLE VARCHAR(100),
    REVIEW_CONTENT VARCHAR(500),
    REVIEW_RATE VARCHAR(50)
);

SELECT 
		*
FROM
        GALLERY_MEMBER;

SELECT 
		*
FROM
        GALLERY_ADMIN;
        
SELECT 
		*
FROM
        GALLERY_SHOP;


SELECT 
		*
FROM
        GALLERY_ARTIST;
        
SELECT 
		*
FROM
        GALLERY_REVIEW;
        
